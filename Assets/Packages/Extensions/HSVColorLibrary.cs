    /// <summary>
    /// HSV 色空間から H が一定の距離で異なる色のセットを取得します。
    /// </summary>
    /// <param name="hueColors">
    /// H が徐々に変化する色のセット。
    /// </param>
    /// <param name="numOfColors">
    /// 取得する色数を指定します。
    /// 指定した数で 360 を割った値が各色の H の距離となります。
    /// </param>
    /// <returns>
    /// <returns>
    /// H 成分が一定の距離を離れた色のセット。
    /// </returns>
    /// </returns>
    private static Color[] GetHueColors360(Color[] hueColors, int numOfColors)
    {
        int span = 359 / numOfColors;

        Color[] colors = new Color[numOfColors];

        for (int i = 0; i < numOfColors; i++)
        {
            colors[i] = hueColors[i * span];
        }

        return colors;
    }

